package university_pg;

public class switch_example {

	public static void main(String[] args) {
		int a = 20;
		switch(a) {
		case 10:
			System.out.println("10입니다.");
			break;
		case 20:
			System.out.println("20입니다.");
			break;
		case 30:
			System.out.println("30입니다.");
			break;
		default:
			System.out.println("확인이 필요한 값입니다.");
		}

	}

}
